package sec1;

public class Crease1 {

	public static void main(String[] args) {
		// 증감 연산
		int a = 5;
		int b = 5;
		System.out.println("(전위증가)++a : "+(++a));
		System.out.println("(후위증가)b++ : "+(b++));
		System.out.println("b : "+(b));
		System.out.println(--b);
		System.out.println(a--);

	}

}
